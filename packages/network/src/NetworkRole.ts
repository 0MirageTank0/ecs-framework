/**
 * 网络组件角色枚举
 * 
 * 定义网络组件在帧同步框架中的角色
 */
export enum NetworkRole {
    /**
     * 客户端角色
     * 
     * 组件将执行客户端特有的逻辑，如：
     * - 输入预测
     * - 状态插值
     * - 回滚重放
     */
    CLIENT = 'client',
    
    /**
     * 服务端角色
     * 
     * 组件将执行服务端特有的逻辑，如：
     * - 输入验证
     * - 权威状态计算
     * - 状态广播
     */
    SERVER = 'server'
}